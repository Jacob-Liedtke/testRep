<p>
It’s our aim to promote sustainability and sustainable practices both locally and globally. 
Throughout the school years, ESW has partnered with many local projects to ensure sustainability incluing Iowa State’s ‘Live Green’ initiative to improve and promote sustainability on campus.  
Currently, ESW members are actively involved in many projects including:
</p>
<ul>
	<li>
		<u>Solar Light</u>- Our solar light project is focused on harnessing solar light to set up a sidewalk lamp that can store the energy and automatically operate in the evenings.
		This project could use more help from individuals with experience in Electrical Engineering, but anyone is welcome to get involved! Please contact Daniel Robinson (isudan@iastate.edu).
	</li>
	<li>
		<u>Rain Garden</u>- Using a plot of land between Carver Hall and the Music Hall, we are implementing a garden to utilize plants that will absorb excessive rainwater runoff, which can cut down the amount of pollution reaching creeks and streams. 
		Please contact Christina Larranga (cjlarra@iastate.edu).
	</li>

