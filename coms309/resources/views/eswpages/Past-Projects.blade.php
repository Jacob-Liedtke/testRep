<b>Uganda</b><br>
Through a partnership with a local Ugandan NGO, Volunteer Efforts for Development Concerns (VEDCO), Makerere University in Kampala, Uganda and the Center for Sustainable Rural Livelihoods (CSRL) at Iowa State University (ISU), ESW members worked on several sustainable development projects such as household energy and water harvesting in the Kamuli District of Uganda. The farmers are subsistence farmers earning about US$100 a year and do not have electricity and proper drinking water in their homes. Students from ESW at ISU have spent the past three summers in Kamuli. They successfully constructed four rainwater harvesting systems and two biogas reactors (which use animal manure to generate energy for lighting).<br>
<br>
<b>Water Harvesting</b><br>
The water related projects were designed for rural farming families. The first water project in 2006 was to collect surface water runoff for irrigation of crops during the dry season ensuring that the crops would be able to be harvested and feed the family. The second water project in 2007 was collecting rainwater off of a roof a in a 1500 L brick tank. Before the collector was installed the family of 8 had to walk 2 kilometers to get filthy water from the Nile River. ESW-ISU has continued to investigate methods of improving the current rooftop collector design. In Summer 2008, two new rooftop systems were constructed.<br>
<br>
The project objectives for Summer 2009 are as follows:
<ul>
	<li>Check and improve on the previously constructed collection systems.</li>
	<li>Improve on current rooftop design to remove &#8220;first flush&#8221; of dirty water.</li>
	<li>Add a filtration system to the existing systems.</li>
	<li>Teach the locals on construction of rooftop systems and household water treatment.</li>
</ul>

