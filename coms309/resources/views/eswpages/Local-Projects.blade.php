		<u>Gasifier</u>-ESW will be beginning a project focused on building a portable biomass gasifier. The gasifier will take wood chips, waste corn seed or a similar material and convert it into methane, hydrogen, and carbon monoxide. 
		The gasifier will introduce people to a green fuel source and increase their knowledge of the process. (3/4/14)
	</li>
	<li>
	<u>Water Sanitation</u>-ESW will also begin investigating water sanitation systems that could be portable, used in a rural home or used in a rural community. We plan to start by looking at current small sink faucet or hose chlorination systems and also investigating alternatives. 
	This project will increase the amount of safe, drinkable water available in rural areas. (3/4/14)
